
class LipSample {

	public static void main(String[] args) {
		
		String str = "Hello";
		
		char ch1 = str.charAt(0);
		char ch2 = str.charAt(1);
		
		int len = str.length();
		
		System.out.println(str + "の1文字目は" + ch1);
		System.out.println(str + "の2文字目は" + ch2);
		System.out.println(str + "の長さは" + len);
	}

}
